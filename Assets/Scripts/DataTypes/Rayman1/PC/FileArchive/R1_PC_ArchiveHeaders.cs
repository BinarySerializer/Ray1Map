        public static byte[] GetHeader(GameSettings settings, string fileName)
        {
            switch (fileName)
            {
                case "VIGNET.DAT":

                    switch (settings.GameModeSelection)
                    {
                        case GameModeSelection.RaymanPC_1_00:
                        case GameModeSelection.RaymanPC_1_10:
                        case GameModeSelection.RaymanPC_1_12:
                        case GameModeSelection.RaymanPC_Demo_2:
                        case GameModeSelection.RaymanPC_1_20:
                        case GameModeSelection.RaymanPC_1_21_JP:
                        case GameModeSelection.RaymanClassicMobile:
                            return VIGNET;

                        case GameModeSelection.RaymanPC_1_21:
                            return VIGNET_1_21;

                        case GameModeSelection.RaymanPC_Demo_1:
                            return VIGNET_Demo1;

                        default:
                            throw new ArgumentOutOfRangeException(nameof(settings.GameModeSelection), settings.GameModeSelection, null);
                    }

                case "SNDD8B.DAT":
                    return settings.GameModeSelection == GameModeSelection.RaymanPC_Demo_1 ? SNDD8B_Demo1 : SNDD8B;
                
                case "SNDH8B.DAT":
                    return settings.GameModeSelection == GameModeSelection.RaymanPC_Demo_1 ? SNDH8B_Demo1 : SNDH8B;
                
                case "SNDVIG.DAT":
                    return SNDVIG;

                case "RAY.LNG":
                    throw new NotImplementedException();

                default:
                    throw new ArgumentOutOfRangeException(nameof(fileName), fileName, null);
            }
        }
    }
